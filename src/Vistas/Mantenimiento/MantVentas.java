    private void CargarClientes(){
        DAOCliente dao = new DAOCliente();
        List<Cliente> lista= dao.Listar();
        Iterator it = lista.iterator();
        while(it.hasNext()){
            Cliente aux = (Cliente) it.next();
            String item = aux.getIdCliente()+" - "+aux.getNombre()+" "+aux.getApellido();
            cbxCliente.addItem(item);
        }
    }
    private void CargarEmpleados(){
        DAOEmpleado dao = new DAOEmpleado();
        List<Usuario> lista = dao.Listar();
        Iterator it=lista.iterator();
        while(it.hasNext()){
            Usuario aux = (Usuario) it.next();
            String item = aux.getCodEmpleado()+" - "+aux.getNombre()+" "+aux.getApellido();
            cbxEmpleado.addItem(item);
        }
    }
    private void CargarProductos(){
        DAOProducto dao = new DAOProducto();
        List<Producto> list = dao.Listar();
        
        for(Producto tmp : list){
            cbxProductos.addItem(tmp.getDescripcion());
        }
    }
    private void Ver_DETVenta( List<DET_Venta> lista){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setRowCount(lista.size());
        modelo.addColumn("Producto");
        modelo.addColumn("Precio Unitario");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio Neto");
        
        Iterator it = lista.iterator();
        int i=0;
        while(it.hasNext()){
           DET_Venta tmp= (DET_Venta) it.next();
           modelo.setValueAt(tmp.getP().getDescripcion(), i, 0);
           modelo.setValueAt(tmp.getP().getPrecioUni(), i, 1);
           modelo.setValueAt(tmp.getCantidad(), i, 2);
           modelo.setValueAt(tmp.getPrecioN(), i, 3);
           i++;
        }
        this.JTableDETV.setModel(modelo);
    }
    private void Actualizar_TablaVenta(){
        DefaultTableModel modelo = new DefaultTableModel();
        DAOVentas dao = new DAOVentas();
        List<Venta> lista = dao.Listar();
        Iterator it=lista.iterator();
        
        modelo.setRowCount(lista.size());
        modelo.addColumn("Numero de Venta");
        modelo.addColumn("Fecha de Emision");
        modelo.addColumn("Precio Total");
        modelo.addColumn("Cliente");
        modelo.addColumn("Empleado");
        
        int i = 0;
        while(it.hasNext()){
            Venta aux = (Venta) it.next();
            Cliente c_tmp = aux.getCliente();
            Usuario e_tmp = aux.getEmpleado();
            
            modelo.setValueAt(aux.getNumVenta(), i, 0);
            modelo.setValueAt(aux.getFechaE(), i, 1);
            modelo.setValueAt(aux.getPrecioT(), i, 2);
            modelo.setValueAt(c_tmp.getNombre()+" "+c_tmp.getApellido(), i, 3);
            modelo.setValueAt(e_tmp.getNombre()+" "+e_tmp.getApellido(), i, 4);
            i++;
        }
        this.JTableVentas.setModel(modelo);
    }
    
